function getCategories(obj) {
  var list = [];

  for (var key in obj) {
    list.push(key);
  }

  return list;
}

var listCategories = getCategories(menu);

function setCategories(data) {
  var list = "\n    ".concat(data.map(function (value) {
    return value === 'pizza' ? " <li class=\"menu__nav-item active\" id=\"".concat(value, "\">").concat(value, "</li>") : " <li class=\"menu__nav-item\" id=\"".concat(value, "\">").concat(value, "</li>");
  }).join('\n'), "\n  ");
  document.querySelector('.menu__nav').innerHTML = list;
}

function getMenuItem(data) {
  return "\n    <li class=\"menu__list-item\">\n      <div class=\"menu__list-title\">\n        <p>".concat(data.name, "</p>\n        <p>. . . .   ").concat(data.price, " USD</p>\n      </div>\n      <p class=\"menu__list-text\">").concat(data.Ingredients, "</p>\n    </li>\n  ");
}

function renderItems(data) {
  var list = "";
  var contain = document.querySelector('.menu>.container');
  console.log(contain);

  if (document.body.clientWidth > 624) {
    contain.classList.contains('small') && contain.classList.remove('small');

    for (var i = 0; i < data.length; i++) {
      list += getMenuItem(data[i]);
    }
  } else {
    !contain.classList.contains('small') && contain.classList.add('small');

    for (var _i = 0; _i < data.length && _i < 8; _i++) {
      list += getMenuItem(data[_i]);
    }
  }

  document.querySelector('.menu__list').innerHTML = list;
}

function Selected(evt) {
  if (!evt.target.classList.contains('active')) {
    var menus = document.querySelectorAll(".menu__nav-item");

    for (var i = 0; i < menus.length; i++) {
      if (menus[i].classList.contains('active')) {
        menus[i].classList.remove('active');
      }
    }

    evt.target.classList.add('active');
    renderItems(menu[evt.target.id]);
  }
}

function Resized(evt) {
  if (evt.target.innerWidth < 690 && evt.target.innerWidth > 600) {
    var list = document.querySelector(".active").id;
    renderItems(menu[list]);
  }
}

window.onload = function () {
  setCategories(listCategories);
  renderItems(menu.pizza);
  window.addEventListener('resize', Resized, false);
  var pizza = document.getElementById("pizza");
  pizza.addEventListener('click', Selected, false);
  var soupe = document.getElementById("soupe");
  soupe.addEventListener('click', Selected, false);
  var pasta = document.getElementById("pasta");
  pasta.addEventListener('click', Selected, false);
  var salads = document.getElementById("salads");
  salads.addEventListener('click', Selected, false);
  var desert = document.getElementById("desert");
  desert.addEventListener('click', Selected, false);
  var drinks = document.getElementById("drinks");
  drinks.addEventListener('click', Selected, false);
};